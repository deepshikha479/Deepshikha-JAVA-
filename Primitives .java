
 public class Primitives {
    public static void main(String[] args) {
        byte b = 10;
        short s = 200;
        int i = 5000;
        long l = 100000L;
        float f = 5.5f;
        double d = 10.123;
        char c = 'D';
        boolean bool = true;

        System.out.println(b + " " + s + " " + i + " " + l);
        System.out.println(f + " " + d + " " + c + " " + bool);
    }
}
